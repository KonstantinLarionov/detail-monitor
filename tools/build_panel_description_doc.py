def set_cell_shading(cell, fill):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), fill)
    tc_pr.append(shd)


def set_cell_text(cell, text, bold=False):
    cell.text = ""
    paragraph = cell.paragraphs[0]
    run = paragraph.add_run(text)
    run.bold = bold
    run.font.name = "Calibri"
    run.font.size = Pt(10)


def add_bullets(doc, items):
    for item in items:
        paragraph = doc.add_paragraph(style="List Bullet")
        paragraph.paragraph_format.space_after = Pt(4)
        paragraph.paragraph_format.line_spacing = 1.167
        run = paragraph.add_run(item)
        run.font.name = "Calibri"
        run.font.size = Pt(11)


def add_section(doc, title, problem, shows, how_to_read, where_to_look, helps):
    doc.add_heading(title, level=1)
    blocks = [
        ("Проблема", problem),
        ("Что показывает", shows),
        ("Как смотреть", how_to_read),
        ("Куда смотреть при разборе", where_to_look),
        ("Что помогает решить", helps),
    ]
    for label, items in blocks:
        paragraph = doc.add_paragraph()
        paragraph.paragraph_format.space_after = Pt(2)
        run = paragraph.add_run(label)
        run.bold = True
        run.font.name = "Calibri"
        run.font.size = Pt(11)
        add_bullets(doc, items)


def configure_styles(doc):
    section = doc.sections[0]
    section.top_margin = Inches(1)
    section.bottom_margin = Inches(1)
    section.left_margin = Inches(1)
    section.right_margin = Inches(1)
    section.header_distance = Inches(0.492)
    section.footer_distance = Inches(0.492)

    styles = doc.styles
    normal = styles["Normal"]
    normal.font.name = "Calibri"
    normal.font.size = Pt(11)
    normal.paragraph_format.space_after = Pt(6)
    normal.paragraph_format.line_spacing = 1.10

    for style_name, size, color, before, after in [
        ("Heading 1", 16, "2E74B5", 16, 8),
        ("Heading 2", 13, "2E74B5", 12, 6),
        ("Heading 3", 12, "1F4D78", 8, 4),
    ]:
        style = styles[style_name]
        style.font.name = "Calibri"
        style.font.size = Pt(size)
        style.font.color.rgb = RGBColor.from_string(color)
        style.paragraph_format.space_before = Pt(before)
        style.paragraph_format.space_after = Pt(after)

    bullet = styles["List Bullet"]
    bullet.font.name = "Calibri"
    bullet.font.size = Pt(11)
    bullet.paragraph_format.left_indent = Inches(0.5)
    bullet.paragraph_format.first_line_indent = Inches(-0.25)
    bullet.paragraph_format.space_after = Pt(8)
    bullet.paragraph_format.line_spacing = 1.167


def add_meta_table(doc):
    table = doc.add_table(rows=4, cols=2)
    table.style = "Table Grid"
    table.autofit = False
    widths = [Inches(1.65), Inches(4.85)]
    rows = [
        ("Назначение", "Детальная диагностика передачи данных, ошибок интеграции, ФЛК и скорости выгрузки объектов в ИИ за выбранную дату и период времени внутри даты."),
        ("Пользователь", "Администратор, аналитик сопровождения, разработчик интеграции, тестировщик."),
        ("Период анализа", "Одна выбранная дата и период времени внутри даты. По умолчанию: с 00:00 до 23:59 выбранного дня."),
        ("Основной принцип", "Детализация строится по RAW-событиям: отправка, ошибки, ФЛК, результаты ИИ и состояния очередей. Сначала проверяются ограничения, ошибки и ФЛК, затем скорость выгрузки и детальная передача по очередям."),
    ]
    for row_index, (label, value) in enumerate(rows):
        row = table.rows[row_index]
        row.cells[0].width = widths[0]
        row.cells[1].width = widths[1]
        set_cell_shading(row.cells[0], "F2F4F7")
        set_cell_text(row.cells[0], label, bold=True)
        set_cell_text(row.cells[1], value)


